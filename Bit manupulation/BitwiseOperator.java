

public class BitwiseOperator {
  public static void main(String args[]){
    System.out.println(5 & 6 );   // use of a binary AND
    System.out.println( 5 | 6);   // use of a binary OR
    System.out.println(5 ^ 6);   // use of a binary XOR
    System.out.println(~5);   // use of  a binary Ones complement
    System.out.println(5<<2);  // use of binary left shift
    System.out.println(6>>2);  // use of binary right shift 

  }
    
}
