
// PRINT 1 TO N USING WHILE LOOP
import java.util.*;
public class print1toN {
 public static void main(String args[]){
    Scanner sc = new Scanner (System.in);

    int count =1;
    int n = sc.nextInt();
    while(count<=n){
        System.out.print(count + " ");
        count++;
    }
 }
    
}

    

