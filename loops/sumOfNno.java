
    import java.util.*;
public class sumOfNno {
 public static void main(String args[]){
    Scanner sc = new Scanner (System.in);

    int count =1; int sum = 0;
    int n = sc.nextInt();
    while(count<=n){
         sum+=count;
        count++;
       
    }
    System.out.println("total sum = " + sum);
 }
    
}

    

